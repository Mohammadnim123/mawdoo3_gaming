/**
 * Deep message shape derived from `en`: string leaves widen to `string`,
 * plural leaves accept any CLDR category set, arrays stay ordered lists.
 * `ar` implements this type — divergence is a compile error.
 */
export type Messages = MessagesOf<typeof en>;

type MessagesOf<T> = T extends string
  ? string
  : T extends readonly string[]
    ? readonly string[]
    : T extends PluralMessage
      ? PluralMessage
      : { readonly [K in keyof T]: MessagesOf<T[K]> };
